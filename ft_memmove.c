#include "libft.h"

void     ft_memmove(void *dest, const void *src, size_t n)
{
    size_t i;
    unsigned char *ptr_src;
    unsigned char *ptr_dest;
    
    ptr_dest = dest;
    ptr_src = (unsigned char *)src;
    i = 0;
    if (ptr_dest >= ptr_src && ptr_dest < ptr_src + n)
    {
        while (n--)
            ptr_dest[n] = ptr_src[n];
    }
    else
    {
        while (i < n)
        {
            ptr_dest[i] = ptr_src[i];
            i++;
        }
    }
}
